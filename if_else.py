#!/usr/bin/env python
#if_else.py

#Iterate over a range of values

for num in range(1,10):

    if num%2==0 :
#Use the modulus operator to get the remainder after division
#If the remainder after dividing by 2 is zero, the number is even else odd
        print (num)
        print ('is even')
        

    else:
        print (num)
        print ('is odd')
        
